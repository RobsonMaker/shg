public void setHospedagem(Hospedagem h) {
        hosp = h;
        if (!h.getFormaPagamento().equals("Dinheiro")) {
            txtTroco.setVisible(false);
            txtValorPagamento.setVisible(false);
            jTValorTroco.setVisible(false);
            jTValorPagamento.setVisible(false);
        } else {
            btFinalizar.setEnabled(false);
            txtAlerta.setVisible(true);
        }

        jTValorTotal.setText("R$: " + h.getPreco_diaria_total());
        jTFormaPagamento.setText(h.getFormaPagamento());
    }
