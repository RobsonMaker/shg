    private void jTValorPagamentoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTValorPagamentoKeyReleased
        // TODO add your handling code here:
         if (!jTValorPagamento.getText().equals("")) {
            if (Double.valueOf(jTValorPagamento.getText()) >= hosp.getPreco_diaria_total()) {
                jTValorTroco.setText("R$: " + (Double.valueOf(jTValorPagamento.getText()) - hosp.getPreco_diaria_total()));
                btFinalizar.setEnabled(true);
                txtAlerta.setVisible(false);
            }else{
                  jTValorTroco.setText("");
                btFinalizar.setEnabled(false);
                txtAlerta.setVisible(true);
            }
        }
    }//GEN-LAST:event_jTValorPagamentoKeyReleased

