    private void btListarPorDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListarPorDataActionPerformed
        // TODO add your handling code here:

        // Imprimindo o relat√É¬≥rio a partir de uma lista de usu√É¬°rios listado pelo controle,
        // ou uma lista associada a uma tabela
        // Primeiro passo - alterar os campos do relat√É¬≥rio no Designer de Relat√É¬≥rio, utilizando
        // os Fields do Report Inspector, adicionando na propriedade Description
        // o nome do atributo na classe. A propriedade Description √É¬© utilizada para 
        // preencher o valor dos Fields com os valores dos objetos existentes na 
        // lista a ser passada para o Relat√É¬≥rio.
        if (contHospedagem.listarPorData(jDateHospedagem.getDate()).isEmpty()) {
            JOptionPane.showMessageDialog(null, "N√£o ha Hospedagens Registradas nesta Data!", "Informa√ß√£o", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                System.out.println(contHospedagem.listarPorData(jDateHospedagem.getDate()));
                // cria um objeto da classe JasperReport, ou seja, um relat√É¬≥rio
                // deve-se passar o caminho onde est√É¬° o relat√É¬≥rio dentro do 
                // seu projeto
                JasperReport relatorioCompilado = JasperCompileManager.compileReport("src/relatorios/Relatorio_Hospedagens.jrxml");

                // Preencher o relat√É¬≥rio com uma lista de usu√É¬°rios passada como um JRBeanCollectionDataSource
                JasperPrint relatorioPreenchido = JasperFillManager.fillReport(relatorioCompilado, null,
                        new JRBeanCollectionDataSource(contHospedagem.listarPorData(jDateHospedagem.getDate())));
                System.out.println(contHospedagem.listarPorData(jDateHospedagem.getDate()));

                // Cria um dialog para colocar o relat√≥rio dentro dele
                JDialog tela = new JDialog(this, "Relat√≥rio de Hospedagens", true);
                tela.setSize(850, 750);
                tela.setLocationRelativeTo(null);

                // criar um componente swing que cont√©m o relatorio
                JRViewer painelRelatorio = new JRViewer(relatorioPreenchido);

                // adiciona o painel com o relat√≥rio dentro do JDialog criado
                tela.getContentPane().add(painelRelatorio);

                // tornar vis√É¬≠vel a janela criada com o relat√É¬≥rio
                tela.setVisible(true);

            } catch (JRException ex) {
                Logger.getLogger(TelaRelatorios.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Erro ao gerar o relat√É¬≥rio.");
            }
        }

    }//GEN-LAST:event_btListarPorDataActionPerformed

