    private void JDateDataNascimentoPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_JDateDataNascimentoPropertyChange
        // TODO add your handling code here:

        if (JDateDataNascimento.getDate() != null) {
            if (contDatas.diferencaDatas(contDatas.dataToString(JDateDataNascimento.getDate()), contDatas.dataToString(new Date())) >= 15) {
                txtValidIdade.setVisible(false);
                btSalvar.setEnabled(true);
            } else {
                txtValidIdade.setVisible(true);
                btSalvar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_JDateDataNascimentoPropertyChange

