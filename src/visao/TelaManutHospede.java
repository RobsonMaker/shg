    public void setHospede(Hospede q) {
        tfNomeHospede.setText(String.valueOf(q.getNome()));
        tfCpfHospede.setText(String.valueOf(q.getCpf()));
        tfRgHospede.setText(String.valueOf(q.getRg()));
        comoEstadoCivil.setSelectedItem(String.valueOf(q.getEstado_sivil()));
        tfTelefoneHospede.setText(String.valueOf(q.getTelefone_cont()));
        tfLougadoro.setText(String.valueOf(q.getEndereco()));
        tfCidade.setText(String.valueOf(q.getCidade()));
        tfCep.setText(String.valueOf(q.getCep()));
        tfUf.setText(String.valueOf(q.getUf()));
        JDateDataNascimento.setDate(q.getData_nascimento());
        this.h = q;
        editar = true;
        //System.out.println(this.q.toString());
    }

