    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        if (tfNomeHospede.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Você não preencheu o nome do Hospede ");
        } else if (tfCpfHospede.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Você não preencheu o cpf do Hospede ");
        } else if (tfLougadoro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Você não preencheu o endereço do Hospede ");
        } else if (tfCidade.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Você não preencheu a cidade do Hospede ");
        } else if (tfCep.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Você não preencheu o CEP");
        } else if (tfUf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Você não preencheu a UF");
        } else if (contDatas.diferencaDatas(contDatas.dataToString(JDateDataNascimento.getDate()), contDatas.dataToString(new Date())) < 15) {
            JOptionPane.showMessageDialog(null, "O hospede deve ter pelo menos 15 anos!");
        } else {

            h.setNome(tfNomeHospede.getText());
            h.setCpf(tfCpfHospede.getText());
            h.setData_nascimento(JDateDataNascimento.getDate());
            h.setRg(tfRgHospede.getText());
            h.setEstado_sivil(comoEstadoCivil.getSelectedItem().toString());
            h.setTelefone_cont(tfTelefoneHospede.getText());
            h.setEndereco(tfLougadoro.getText());
            h.setCidade(tfCidade.getText());
            h.setCep(tfCep.getText());
            h.setUf(tfUf.getText());

            if (editar) {
                contHospede.alterar(h);
            } else {

                contHospede.inserir(h);
            }
            limparCampo();

            dispose();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

