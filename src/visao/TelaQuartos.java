    private void tfPesquisarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfPesquisarKeyReleased
        // TODO add your handling code here:
        if (tfPesquisar.getText().equals("")) {
            atualizaTabela(true);
        } else {
            listaQuartos.clear();

            switch (jComboFiltro.getSelectedItem().toString()) {
                case "Por Número":
                    listaQuartos.addAll(contQuarto.listarPorNumero(Integer.valueOf(tfPesquisar.getText())));
                    break;
                case "Por Tipo":
                    listaQuartos.addAll(contQuarto.listarPorTipo(tfPesquisar.getText()));
                    break;
                case "Por Status":
                    listaQuartos.addAll(contQuarto.listarPorStatus(tfPesquisar.getText()));
                    break;
            }

            atualizaTabela(false);
        }

    }//GEN-LAST:event_tfPesquisarKeyReleased

