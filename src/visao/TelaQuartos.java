    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int posicao = jTQuartos.getSelectedRow();

        if (jTQuartos.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Você Não Selecionou Nenhum Quarto para excluir!");
        } else {
            Quarto q = listaQuartos.get(posicao);

            int input = JOptionPane.showConfirmDialog(this,
                    "Tem certeza que dejesa excluir este quarto?", "Confirmação de exclusão",
                    JOptionPane.OK_CANCEL_OPTION);

            if (input == 0) {

                try {
                    contQuarto.excluir(q);
                    atualizaTabela(true);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Não é possível excluir este Quarto, pois faz parte de uma hospedagem realizada!");
                }

            }

        }
// TODO add your handling code here:
    }//GEN-LAST:event_btExcluirActionPerformed

